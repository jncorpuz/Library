    void checkID(){
        String sqlStatement = "select * from userData where id ="+txtID.getText()+";";
         try {
            Connection dbCon = Database.DBConnection();
            Statement dbCom = dbCon.createStatement();
            ResultSet sqlRecord = dbCom.executeQuery(sqlStatement);
            if (sqlRecord != null && sqlRecord.next()) {
                do {
                    txtUser1.setText(sqlRecord.getString("userName"));
                    txtPass1.setText(sqlRecord.getString("password"));
                    txtFirst1.setText(sqlRecord.getString("firstName"));
                    txtLast1.setText(sqlRecord.getString("lastName"));
                    cmbType1.setSelectedItem(sqlRecord.getString("userType"));
                } while (sqlRecord.next());
            } else {
                JOptionPane.showMessageDialog(null, "ID Does not exist");
            }
            enableEdit();
            dbCom.close();
            dbCon.close();
        }
       catch (SQLException e)
       {
           JOptionPane.showMessageDialog(null, e.getMessage());
       }
       catch (Exception e)
       {
           JOptionPane.showMessageDialog(null, e.getMessage());
       }
        
    }
    
