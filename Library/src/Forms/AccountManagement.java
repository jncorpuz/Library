    void checkValues(){
        String msg = "";
        if (txtFirst.getText().isEmpty())
        {
            msg +="FirstName is Empty\n";
        }
        if (txtLast.getText().isEmpty())
        {
            msg +="LastName is Empty\n";
        }
        if (txtUser.getText().isEmpty())
        {
            msg +="UserName is Empty\n";
        }
        if (txtPass.getPassword().length == 0)
        {
            msg +="Password is Empty\n";
        }
        if (cmbType.getSelectedIndex() == -1){
            msg +="Type is Empty\n";
        }
        if (msg != ""){
            JOptionPane.showMessageDialog(null, msg);
        }
        else{
            saveData();
            clrAddAll();
        }
       
    }
    
