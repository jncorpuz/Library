    void updateData(){
        String pass = new String(txtPass1.getPassword());
        String sqlStatement = "update userData set userName = '"+txtUser1.getText()+"', firstName ='"+txtFirst1.getText()+"', lastName = '"+txtLast1.getText()+"', password = '"+pass+"', userType = '"+cmbType1.getSelectedItem()+"' where id="+txtID.getText()+";";
        Database n = new Database();
        n.ExecuteStatement(sqlStatement);
        getTable();
    }
    
