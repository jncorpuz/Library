    void getTable(){
        try {
            String sqlStatement = "select ID,userName as Username, firstName as FirstName, lastName as LastName, userType as UserType from userData";
            Connection dbCon = Database.DBConnection();
            Statement dbCom = dbCon.createStatement();
            ResultSet sqlRecord = dbCom.executeQuery(sqlStatement);
            jTable2.setModel(DbUtils.resultSetToTableModel(sqlRecord));
            dbCom.execute(sqlStatement);
            dbCom.close();
            dbCon.close();
        }
       catch (SQLException e)
       {
           JOptionPane.showMessageDialog(null, e.getMessage());
       }
       catch (Exception e)
       {
           JOptionPane.showMessageDialog(null, e.getMessage());
       }
    
    }
    
