    void checkID(){
         try 
        {
            LocalDate localDate = LocalDate.now();
            String sqlStatement = "select concat(firstName,\" \", lastName) as fullName from userdata where id ="+txtID.getText()+";";
            Connection dbCon = DriverManager.getConnection(connectionStr, username, password);
            Statement dbCom = dbCon.createStatement();
            ResultSet sqlRecord = dbCom.executeQuery(sqlStatement);
            if (sqlRecord != null && sqlRecord.next()) {
                txtDate.setText(DateTimeFormatter.ofPattern("yyy-MM-dd").format(localDate));
                txtMsg.setEnabled(true);
                do {
                   lblName.setText(sqlRecord.getString("fullName"));
                } while (sqlRecord.next());
            } else {
                JOptionPane.showMessageDialog(null, "ID Does not exist");
            }
            dbCom.close();
            dbCon.close();
        }
        catch (SQLException e)
        {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
