    void checkData() {
        String msg = "";
        if (txtDate.getText().isEmpty()) {
            msg += "date is empty";
        }
        if (msg != "") {
            JOptionPane.showMessageDialog(null, msg);
        } else {
            saveData();
        }
    }
    
    void checkEditData() {
        String msg = "";
        if (txtDate1.getText().isEmpty()) {
            msg += "date is empty";
        }
        if (msg != "") {
            JOptionPane.showMessageDialog(null, msg);
        } else {
            update();
        }
    }
    
    void getData(){
     try 
        {
            String sqlStatement  = "select * from holiday";
            Connection dbCon = Database.DBConnection();
            Statement dbCom = dbCon.createStatement();
            ResultSet sqlRecord = dbCom.executeQuery(sqlStatement);
            jTable3.setModel(DbUtils.resultSetToTableModel(sqlRecord));
            dbCom.close();
            dbCon.close();
        
        }
        catch (SQLException e)
        {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
    void checkID() {
        String sqlStatement = "select * from Holiday where id =" + txtID.getText() + ";";
        try {
            Connection dbCon = Database.DBConnection();
            Statement dbCom = dbCon.createStatement();
            ResultSet sqlRecord = dbCom.executeQuery(sqlStatement);
            if (sqlRecord != null && sqlRecord.next()) {
                do {
                    cmdDel.setEnabled(true);
                    cmdUpd.setEnabled(true);
                    txtDate1.setEnabled(true);
                    txtDate1.setText(sqlRecord.getString("holidayDate"));
                } while (sqlRecord.next());
            } else {
                JOptionPane.showMessageDialog(null, "ID Does not exist");
            }
            dbCom.close();
            dbCon.close();
            getData();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
    void clrAll()
    {
        txtID.setText("");
        txtDate1.setText("");
    }
    void delete(){
         String sqlStatement = "delete from holiday where id ="+txtID.getText()+";";
         Database n = new Database();
         n.ExecuteStatement(sqlStatement);
         txtID.setText("");
         getData();
         disableAll();
    }
    
    void update(){
        String sqlStatement =" update holiday set holidayDate ='"+txtDate1.getText()+"' where id="+txtID.getText()+";";
        Database n = new Database();
        n.ExecuteStatement(sqlStatement);
    }
    
    void disableAll() {        
        cmdDel.setEnabled(false);
        cmdUpd.setEnabled(false);
        txtDate1.setEnabled(false);
        txtDate1.setText("");
    }
    
    void saveData() {
        String sqlStatement = "insert into holiday( holidayDate) values('"+txtDate.getText() + "')";
        Database n = new Database();
        n.ExecuteStatement(sqlStatement);
        getData();
    }
    
